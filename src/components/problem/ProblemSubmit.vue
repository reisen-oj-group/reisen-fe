function getExtension(fileName: string): string {
  // 处理没有扩展名的情况
  if (!fileName.includes('.')) return ''
  
  // 分割文件名获取扩展名
  const parts = fileName.split('.')
  return '.' + parts[parts.length - 1].toLowerCase()
}

